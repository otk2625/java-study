package star1;

public class StarUnit {
	protected String name; //heap
	protected int hp;
	protected int attack;
	
	protected StarUnit() {
		
	}
	protected StarUnit(String n, int h, int a) {
		
	}
	protected String getName() {
		return name;
	}
	protected void setName(String name) {
		this.name = name;
	}
	protected int getHp() {
		return hp;
	}
	protected void setHp(int hp) {
		this.hp = hp;
	}
	protected int getAttack() {
		return attack;
	}
	protected void setAttack(int attack) {
		this.attack = attack;
	}
}
	
